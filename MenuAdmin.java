    private void btnPrintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrintActionPerformed
        SwingUtilities.invokeLater(new Runnable(){
            @Override
            public void run(){
                try {
                        SqlStatement s = new SqlStatement();
                        List<String> Admins = s.select_admin();

                        if (!Admins.isEmpty()){
                            jEditorPane1.setText(Admins.get(0));
                            jEditorPane1.setText(jEditorPane1.getText()+ "\t" + Admins.get(1));
                            int i = 2;
                            while(i<Admins.size()-1){
                                jEditorPane1.setText(jEditorPane1.getText() + "\n" + Admins.get(i));
                                jEditorPane1.setText(jEditorPane1.getText() + "\t" + Admins.get(i+1));
                                i++;
                            }
                        } else{
                            jEditorPane1.setText("No Data");
                        }
                        jEditorPane1.setText(jEditorPane1.getText() + "\n");
                    }
                    catch (SQLException e){

                    }
            }
        });
        
        lblinfo.setVisible(true);
        jEditorPane1.addMouseListener(new MouseListener(){
            @Override
            public void mouseClicked(MouseEvent e) {
                Point pt = new Point(e.getX(),e.getY());
                int i = jEditorPane1.viewToModel(pt);
                jLabel1.setText("");
                btnSearch.setEnabled(false);
                
                /* Get Name */
                if(!Character.isDigit(jEditorPane1.getText().charAt(i))){
                    while(!String.valueOf(jEditorPane1.getText().charAt(i)).equals("\t")){
                        i--;
                    }
                    i++;
                    ArrayList<Character> temp;
                    temp = new ArrayList<>();
                    while(!String.valueOf(jEditorPane1.getText().charAt(i)).equals("\n")){
                        temp.add(jEditorPane1.getText().charAt(i));
                        i++;
                    }
                    StringBuilder SB = new StringBuilder(temp.size());
                    for(Character c : temp){
                        SB.append(c);
                    }
                    
                    jLabel1.setText(SB.toString());
                    btnSearch.setEnabled(true);
                }
            }

            @Override
            public void mousePressed(MouseEvent e) {
            }

            @Override
            public void mouseReleased(MouseEvent e) {
            }

            @Override
            public void mouseEntered(MouseEvent e) {
            }

            @Override
            public void mouseExited(MouseEvent e) {
            }
        });
    }//GEN-LAST:event_btnPrintActionPerformed

